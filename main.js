const Header = React.createClass({
    render() {
        return (
            <h1 className="page__header">React Gifs</h1>
        );
    }
});

const GifsList = React.createClass({
    render() {
        return (
            <div>
                {
                    LinksList.map((gifItem, index) =>
                        <img
                            key={index}
                            src={gifItem}
                            style={{height: 90, width: 'auto'}}
                        />
                    )
                }
            </div>
        );
    }
});

const RangomImage = React.createClass({
    getInitialState() {
        return {
            gifURL: this.getRandomGif()
        }
    },

    getRandomGif() {
        return LinksList[Math.floor(Math.random() * LinksList.length)];
    },

    setRandomGif() {
        this.setState({gifURL: this.getRandomGif()})
    },

    render() {
        return (
            <div
                className='main-gif__container'
                onClick={this.setRandomGif}
            >
                <img
                    alt="Main image"
                    className='main-gif__image'
                    src={this.state.gifURL}
                />
            </div>
        );
    }
});

ReactDOM.render(
    <div>
        <Header />
        <RangomImage/>
        <GifsList/>
    </div>
    ,
    document.getElementById('root')
);